package com.example.pmd_l2;

public class MyListData {
    private String name;
    private int ImageId;

    public MyListData(String name, int imgId) {
        this.name = name;
        this.ImageId = imgId;
    }


    public String getName() {
        return name;
    }

    public int getImageId() {
        return ImageId;
    }

}
